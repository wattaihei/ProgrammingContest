//累乗
mint modpow(const mint &a,ll n){
    if(n==0)return 1;
    mint t=modpow(a,n/2);
    t=t*t;
    if(n&1)t=t*a;
    return t;
}

struct dsu {
  public:
    dsu() : _n(0) {}
    dsu(int n) : _n(n), parent_or_size(n, -1) {}

    int merge(int a, int b) {
        assert(0 <= a && a < _n);
        assert(0 <= b && b < _n);
        int x = leader(a), y = leader(b);
        if (x == y) return x;
        if (-parent_or_size[x] < -parent_or_size[y]) std::swap(x, y);
        parent_or_size[x] += parent_or_size[y];
        parent_or_size[y] = x;
        return x;
    }

    bool same(int a, int b) {
        assert(0 <= a && a < _n);
        assert(0 <= b && b < _n);
        return leader(a) == leader(b);
    }

    int leader(int a) {
        assert(0 <= a && a < _n);
        if (parent_or_size[a] < 0) return a;
        return parent_or_size[a] = leader(parent_or_size[a]);
    }

    int size(int a) {
        assert(0 <= a && a < _n);
        return -parent_or_size[leader(a)];
    }

    std::vector<std::vector<int>> groups() {
        std::vector<int> leader_buf(_n), group_size(_n);
        for (int i = 0; i < _n; i++) {
            leader_buf[i] = leader(i);
            group_size[leader_buf[i]]++;
        }
        std::vector<std::vector<int>> result(_n);
        for (int i = 0; i < _n; i++) {
            result[i].reserve(group_size[i]);
        }
        for (int i = 0; i < _n; i++) {
            result[leader_buf[i]].push_back(i);
        }
        result.erase(
            std::remove_if(result.begin(), result.end(),
                           [&](const std::vector<int>& v) { return v.empty(); }),
            result.end());
        return result;
    }

  private:
    int _n;
    // root node: -1 * component size
    // otherwise: parent
    std::vector<int> parent_or_size;
};

void solve() {

    int N, M; cin >> N >> M;
    dsu uni(M);
    vector<int> B(M, 0);
    int k, x, y;
    vector<int> ans;
    for (int i=0; i<N; i++) {
        cin >> k;
        if (k == 1) {
            cin >> x; x--;
            x = uni.leader(x);
            if (B[x] == 0) {
                B[x] = 1;
                ans.push_back(i+1);
            }
        } else {
            cin >> x >> y;
            x--; y--;
            x = uni.leader(x);
            y = uni.leader(y);
            if (x != y && (B[x] == 0 || B[y] == 0)) {
                if (B[x] == 1 || B[y] == 1) {
                    B[x] = 1;
                    B[y] = 1;
                }
                uni.merge(x, y);
                ans.push_back(i+1);
            }
        }
    }

    int l = ans.size();
    cout << modpow(2, l) << " " << l << endl;
    for (int a : ans) {
        cout << a << " ";
    }

    cout << endl;
    
    return;
}

